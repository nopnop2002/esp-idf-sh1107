// convert from 64x128 image to 128x64 image
void convertImage(uint8_t *srcImage, uint8_t *dstImage) {
	uint8_t dst[16][8];
	memset(dst, 0, 16*8);
	int offset = 56;
	for (int page=0;page<8;page++) {
		int dstPage = 0;
		int dstBits = 0;
		for (int seg=0;seg<128;seg++) {
			uint8_t src = sh1107_rotate_byte(srcImage[seg*8+page]);
			int dstSegs = 7;
			for (int srcBits=0;srcBits<8;srcBits++) {
				uint8_t wk = dst[dstPage][dstSegs];
				uint8_t wk2 = sh1107_copy_bit(src, srcBits, wk, dstBits);
				//ESP_LOGI(TAG, "src=0x%x srcBits=%d dstPage=%d dstSegs=%d wk=0x%x dstBits=%d wk2=0x%x", src, srcBits, dstPage, dstSegs, wk, dstBits, wk2);
				dst[dstPage][dstSegs] = wk2;
				dstSegs--;
			}

			dstBits++;
			if (dstBits == 8) {
				dstBits = 0;
				dstPage++;
			}
		}

		for (int _page=0;_page<16;_page++) {
			for (int _seg=0;_seg<8;_seg++) {
				dstImage[_page*64+_seg+offset] = dst[_page][_seg];
			}
		}
		offset = offset - 8;
	}
}


