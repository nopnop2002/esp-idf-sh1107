void app_main(void)
{
	SH1107_t dev;

#if CONFIG_I2C_INTERFACE
	ESP_LOGI(TAG, "INTERFACE is i2c");
	ESP_LOGI(TAG, "CONFIG_SDA_GPIO=%d",CONFIG_SDA_GPIO);
	ESP_LOGI(TAG, "CONFIG_SCL_GPIO=%d",CONFIG_SCL_GPIO);
	ESP_LOGI(TAG, "CONFIG_RESET_GPIO=%d",CONFIG_I2C_RESET_GPIO);
	i2c_master_init(&dev, CONFIG_SDA_GPIO, CONFIG_SCL_GPIO, CONFIG_I2C_RESET_GPIO);
#endif // CONFIG_I2C_INTERFACE

#if CONFIG_SPI_INTERFACE
	ESP_LOGI(TAG, "INTERFACE is SPI");
	ESP_LOGI(TAG, "CONFIG_MOSI_GPIO=%d",CONFIG_MOSI_GPIO);
	ESP_LOGI(TAG, "CONFIG_SCLK_GPIO=%d",CONFIG_SCLK_GPIO);
	ESP_LOGI(TAG, "CONFIG_CS_GPIO=%d",CONFIG_CS_GPIO);
	ESP_LOGI(TAG, "CONFIG_DC_GPIO=%d",CONFIG_DC_GPIO);
	ESP_LOGI(TAG, "CONFIG_RESET_GPIO=%d",CONFIG_SPI_RESET_GPIO);
	spi_master_init(&dev, CONFIG_MOSI_GPIO, CONFIG_SCLK_GPIO, CONFIG_CS_GPIO, CONFIG_DC_GPIO, CONFIG_SPI_RESET_GPIO);
#endif // CONFIG_SPI_INTERFACE

	sh1107_init(&dev, 64, 128);
	sh1107_contrast(&dev, 0xff);

	// Allocate memory
	uint8_t *buffer = (uint8_t *)malloc(8*128); // 8 page 128 pixel
	if (buffer == NULL) {
		ESP_LOGE(TAG, "malloc failed");
		while(1) { vTaskDelay(1); }
	}
	uint8_t *segmentImage = (uint8_t *)malloc(IMAGES*8*128); // IMAGES * 8 page 128 pixel
	if (segmentImage == NULL) {
		ESP_LOGE(TAG, "malloc failed");
		while(1) { vTaskDelay(1); }
	}

	// Convert from segmentDisplay to segmentImage
	for (int imageIndex=0;imageIndex<IMAGES;imageIndex++) {
		sh1107_clear_screen(&dev, false);
		sh1107_bitmaps(&dev, imageOffset[imageIndex], 0, cute_cat[imageIndex], 64, 64, false);
		sh1107_get_buffer(&dev, buffer);
		int segmentImageIndex = imageIndex * 8 * 128;
		for (int page=0;page<8;page++) {
			memcpy(&segmentImage[segmentImageIndex+page*128], &buffer[page*128], 128);
		}
		vTaskDelay(100);
	}

	while (1) {
		for (int imageIndex=0;imageIndex<IMAGES;imageIndex++) {
			int segmentImageIndex = imageIndex * 8 * 128;
			sh1107_set_buffer(&dev, &segmentImage[segmentImageIndex]);
			sh1107_show_buffer(&dev);
			vTaskDelay(10);
		}
	}
}
